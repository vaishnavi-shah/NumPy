import numpy as np

# creating a 0D numpy array
n = np.array(25) # single element
print(n)
print("Dimensions = ",n.ndim)

# creating a 1D numpy array
q = np.array([1,2,3]) # list
print(q)
print("Dimension = ",q.ndim)

#creating a 2D numpy array
p = np.array([[1,2,3,],[4,5,6]]) # matrix
print(p)
print("Dimension = ",p.ndim)
